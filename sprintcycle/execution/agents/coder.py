# ==================== Bug 分析与修复方法 ====================

async def analyze_and_fix(self, error_log: str, context: "AgentContext", feedback_loop=None) -> "AgentResult":
    """
    分析错误并尝试修复
    
    采用渐进式策略：
    1. 静态分析 → 快速定位问题（无需 LLM）
    2. 错误模式匹配 → 查找历史解决方案
    3. LLM 分析 → 复杂问题深度分析
    
    Args:
        error_log: 错误日志内容
        context: 执行上下文
        feedback_loop: FeedbackLoop 实例（可选）
        
    Returns:
        AgentResult: 包含修复方案或解释的执行结果
    """
    import time
    start_time = time.time()
    
    logger.info(f"🔍 开始分析错误日志，长度: {len(error_log)} 字符")
    
    fix_suggestion = None
    explanation = ""
    analysis_method = "none"
    static_results = []
    matched_patterns = []
    
    # 1. 静态分析
    if context.config.get("use_static_analysis", True):
        static_results = await self._run_static_analysis(context)
        if static_results:
            logger.info(f"📊 静态分析发现 {len(static_results)} 个问题")
            static_summary = self._summarize_static_results(static_results)
            explanation = f"静态分析结果：{static_summary}"
            analysis_method = "static"
    
    # 2. 错误模式匹配
    if feedback_loop:
        matched_patterns = feedback_loop.match_patterns(error_log)
        if matched_patterns:
            best_pattern = matched_patterns[0]
            if best_pattern.confidence >= 0.5:
                logger.info(f"📝 匹配到历史模式: {best_pattern.error_type} (置信度: {best_pattern.confidence:.0%})")
                values = best_pattern.extract_values(error_log)
                fix_suggestion = best_pattern.suggested_fix
                for key, value in values.items():
                    fix_suggestion = fix_suggestion.replace(f"\\{key}", value)
                
                if analysis_method == "static":
                    explanation += f"\n\n历史模式匹配: {best_pattern.error_type}\n根因: {best_pattern.root_cause}"
                else:
                    explanation = f"模式匹配: {best_pattern.error_type}\n根因: {best_pattern.root_cause}"
                    analysis_method = "pattern"
    
    # 3. LLM 分析（兜底）
    if not fix_suggestion and context.config.get("use_llm_fallback", True):
        logger.info("🤖 使用 LLM 分析错误")
        llm_result = await self._llm_analyze_error(error_log, context, static_results)
        fix_suggestion = llm_result.get("fix_suggestion")
        explanation = llm_result.get("explanation", explanation)
        analysis_method = "llm"
    
    if not fix_suggestion:
        explanation = explanation or "未能确定修复方案"
    
    # 记录到反馈循环
    if feedback_loop and fix_suggestion:
        feedback_loop.record_error_and_fix(
            error=error_log,
            fix=fix_suggestion,
            success=False,
            metadata={
                "analysis_method": analysis_method,
                "matched_patterns": len(matched_patterns),
                "static_results_count": len(static_results),
            }
        )
    
    duration = time.time() - start_time
    
    return AgentResult(
        success=bool(fix_suggestion),
        output=explanation,
        artifacts={
            "fix": fix_suggestion,
            "analysis_method": analysis_method,
            "static_results": [r.to_dict() for r in static_results],
            "matched_patterns": [p.to_dict() for p in matched_patterns],
        },
        metrics={
            "analysis_duration": round(duration, 2),
            "has_fix": bool(fix_suggestion),
        },
        feedback=f"[分析完成] 方法: {analysis_method}, 耗时: {duration:.1f}s",
    )

async def _run_static_analysis(self, context: "AgentContext") -> list:
    """运行静态代码分析"""
    try:
        from ..static_analyzer import StaticAnalyzer, AnalysisConfig
        
        project_path = context.project_path or "."
        config = AnalysisConfig(
            ruff_enabled=True,
            ruff_rules=["E", "F", "W", "I", "UP"],
            mypy_enabled=context.config.get("use_mypy", False),
        )
        
        analyzer = StaticAnalyzer(project_path, config)
        results = await analyzer.analyze_python()
        return results
    except ImportError:
        logger.warning("静态分析模块未安装，跳过")
        return []
    except Exception as e:
        logger.warning(f"静态分析失败: {e}")
        return []

def _summarize_static_results(self, results: list) -> str:
    """总结静态分析结果"""
    if not results:
        return "未发现问题"
    
    errors = [r for r in results if r.severity == "error"]
    warnings = [r for r in results if r.severity == "warning"]
    
    summary_parts = []
    if errors:
        summary_parts.append(f"{len(errors)} 个错误")
    if warnings:
        summary_parts.append(f"{len(warnings)} 个警告")
    
    top_issues = results[:3]
    if top_issues:
        issue_lines = [f"  - {r.file_path}:{r.line} [{r.code}] {r.message[:50]}" 
                     for r in top_issues]
        summary_parts.append("\n" + "\n".join(issue_lines))
    
    return ", ".join(summary_parts) if summary_parts else "未发现问题"

